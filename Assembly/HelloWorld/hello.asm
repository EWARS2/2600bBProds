;------------------------------------------------------------

	ORG $1000

Start
	SEI		; Initialize the machine, set interrupt disable.
	CLD		; Clear decimal mode.
	LDX #$FF	; Reset the stack pointer.
	TXS

	INX		; NOTE: sets X to $00
	TXA		; NOTE: sets A to $00

ClearRAM
	STA 0,X		; Clear TIA registers and RAM
	INX		;
	BNE ClearRAM	;
			; Note, the init leaves the background color black.


;------------------------------------------------------------
	LDA #2    
	STA VBLANK		;turn off video output

	LDA #0
	STA AUDC0
	STA AUDF0

	LDY #0			;Y needs to be initialized to zero.
	STY SoundPtrLo
	LDA #>HelloStart
	STA SoundPtrHi

PlayNextPage
SampleLoop
	LDA (SoundPtrLo),Y
	STA AUDV0		;Play the lower nibble of the sample.

	JSR Wait

	LSR
	LSR
	LSR
	LSR
	STA AUDV0		;Play the higher nibble of the sample.

#	JSR Wait

	INY
	BNE SampleLoop

	INC SoundPtrHi
	LDA SoundPtrHi
	CMP (#>HelloStart + 9)
	BNE PlayNextPage

